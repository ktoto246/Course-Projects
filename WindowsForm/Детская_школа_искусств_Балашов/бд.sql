-- Создание базы данных Детская школа искусств №1 г. Балашова
CREATE DATABASE Детская_школа_искусств_Балашов;
GO

USE Детская_школа_искусств_Балашов;
GO

-- Таблица сотрудников
CREATE TABLE сотрудники (
    id_сотрудника INT IDENTITY(1,1) PRIMARY KEY,
    фамилия NVARCHAR(50) NOT NULL,
    имя NVARCHAR(50) NOT NULL,
    отчество NVARCHAR(50),
    должность NVARCHAR(100) NOT NULL,
    телефон NVARCHAR(20),
    email NVARCHAR(100),
    дата_приема DATE NOT NULL,
    CHECK (должность IN ('преподаватель', 'концертмейстер', 'администратор', 'директор', 'завуч')),
    CHECK (телефон LIKE '+7([0-9][0-9][0-9])[0-9][0-9][0-9]-[0-9][0-9]-[0-9][0-9]')
);
GO

-- Таблица отделений
CREATE TABLE отделения (
    id_отделения INT IDENTITY(1,1) PRIMARY KEY,
    название_отделения NVARCHAR(100) NOT NULL,
    заведующий_отделением INT NOT NULL,
    этаж INT NOT NULL,
    кабинет NVARCHAR(10) NOT NULL,
    CHECK (название_отделения IN ('фортепианное', 'струнное', 'народное', 'хоровое', 'хореографическое', 'театральное')),
    CHECK (этаж BETWEEN 1 AND 4),
    FOREIGN KEY (заведующий_отделением) REFERENCES сотрудники(id_сотрудника)
);
GO

-- Таблица учащихся
CREATE TABLE учащиеся (
    id_учащегося INT IDENTITY(1,1) PRIMARY KEY,
    фамилия NVARCHAR(50) NOT NULL,
    имя NVARCHAR(50) NOT NULL,
    отчество NVARCHAR(50),
    дата_рождения DATE NOT NULL,
    класс INT NOT NULL,
    id_отделения INT NOT NULL,
    id_преподавателя INT NOT NULL,
    год_поступления INT NOT NULL,
    CHECK (класс BETWEEN 1 AND 11),
    CHECK (год_поступления >= 2000 AND год_поступления <= YEAR(GETDATE())),
    CHECK (YEAR(дата_рождения) BETWEEN 1990 AND 2020),
    FOREIGN KEY (id_отделения) REFERENCES отделения(id_отделения),
    FOREIGN KEY (id_преподавателя) REFERENCES сотрудники(id_сотрудника)
);
GO

-- Таблица расписание
CREATE TABLE расписание (
    id_занятия INT IDENTITY(1,1) PRIMARY KEY,
    id_преподавателя INT NOT NULL,
    id_учащегося INT NOT NULL,
    id_отделения INT NOT NULL,
    день_недели NVARCHAR(15) NOT NULL,
    время_начала TIME NOT NULL,
    время_окончания TIME NOT NULL,
    предмет NVARCHAR(100) NOT NULL,
    кабинет NVARCHAR(10) NOT NULL,
    CHECK (день_недели IN ('понедельник', 'вторник', 'среда', 'четверг', 'пятница', 'суббота')),
    CHECK (время_начала < время_окончания),
    CHECK (DATEPART(HOUR, время_начала) BETWEEN 8 AND 20),
    FOREIGN KEY (id_преподавателя) REFERENCES сотрудники(id_сотрудника),
    FOREIGN KEY (id_учащегося) REFERENCES учащиеся(id_учащегося),
    FOREIGN KEY (id_отделения) REFERENCES отделения(id_отделения)
);
GO

