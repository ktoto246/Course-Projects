CREATE DATABASE Балтекс;
GO
USE Балтекс;
GO
CREATE TABLE Продукция (
    Код_Продукта INT IDENTITY(1,1) PRIMARY KEY,
    Артикул NVARCHAR(50) NOT NULL UNIQUE,
    Наименование NVARCHAR(255) NOT NULL,
    Тип_ткани NVARCHAR(100) NOT NULL,
    Ширина DECIMAL(10, 2) NOT NULL,
    Плотность INT,
    Состав NVARCHAR(255),
    Цена DECIMAL(10, 2) NOT NULL,
    Количество_на_складе DECIMAL(10, 2) DEFAULT 0,
    Минимальный_запас DECIMAL(10, 2) DEFAULT 100
);
GO
CREATE TABLE Клиенты (
    Код_Клиента INT IDENTITY(1,1) PRIMARY KEY,
    Название NVARCHAR(255) NOT NULL,
    ИНН NVARCHAR(12),
    Телефон NVARCHAR(20),
    Email NVARCHAR(100),
    Контактное_лицо NVARCHAR(255),
    Адрес NVARCHAR(500)
);
GO

CREATE TABLE Заказы (
    Код_Заказа INT IDENTITY(1,1) PRIMARY KEY,
    Номер NVARCHAR(50) NOT NULL UNIQUE,
    Код_клиента INT NOT NULL,
    Код_продукта INT NOT NULL,
    Дата DATE,
    Статус NVARCHAR(50) DEFAULT 'Новый',
    Количество DECIMAL(10, 2) NOT NULL,
    Цена DECIMAL(10, 2) NOT NULL,
    Общая_сумма DECIMAL(15, 2) DEFAULT 0,
    Примечание NVARCHAR(1000),
    CONSTRAINT FK_Заказы_Клиенты FOREIGN KEY (Код_клиента) 
        REFERENCES Клиенты(Код_Клиента),
    CONSTRAINT FK_Заказы_Продукция FOREIGN KEY (Код_продукта) 
        REFERENCES Продукция(Код_Продукта),
    CONSTRAINT CHK_Количество CHECK (Количество > 0),
    CONSTRAINT CHK_Цена CHECK (Цена >= 0)
);
GO
