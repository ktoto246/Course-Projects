CREATE DATABASE БАЛКОМХЛЕБПРОДУКТ;
GO
USE БАЛКОМХЛЕБПРОДУКТ;
GO
CREATE TABLE сотрудники (
    id_сотрудника INT IDENTITY(1,1) PRIMARY KEY,
    фамилия NVARCHAR(50) NOT NULL,
    имя NVARCHAR(50) NOT NULL,
    отчество NVARCHAR(50),
    должность NVARCHAR(50) NOT NULL,
    телефон NVARCHAR(20) NOT NULL,
    email NVARCHAR(100),
    дата_приема DATE NOT NULL DEFAULT GETDATE(),
    CHECK (LEN(фамилия) >= 2),
    CHECK (LEN(имя) >= 2),
    CHECK (LEN(телефон) >= 6),
    CHECK (должность IN ('менеджер по продажам', 'логист', 'кладовщик', 'водитель', 'бухгалтер', 'директор'))
);
GO
CREATE TABLE предприятия_розничной_торговли (
    id_предприятия INT IDENTITY(1,1) PRIMARY KEY,
    название NVARCHAR(100) NOT NULL,
    адрес NVARCHAR(200) NOT NULL,
    телефон NVARCHAR(20) NOT NULL,
    тип_предприятия NVARCHAR(50) NOT NULL,
    email NVARCHAR(100),
    CHECK (LEN(название) >= 3),
    CHECK (LEN(адрес) >= 10),
    CHECK (LEN(телефон) >= 6),
    CHECK (тип_предприятия IN ('магазин', 'супермаркет', 'гипермаркет', 'мини-маркет', 'торговая точка'))
);
GO
CREATE TABLE продукты (
    id_продукта INT IDENTITY(1,1) PRIMARY KEY,
    наименование NVARCHAR(100) NOT NULL,
    категория NVARCHAR(50) NOT NULL,
    единица_измерения NVARCHAR(20) NOT NULL,
    цена_за_единицу DECIMAL(10,2) NOT NULL,
    срок_годности_дни INT NOT NULL,
    описание NVARCHAR(500),
    CHECK (LEN(наименование) >= 2),
    CHECK (цена_за_единицу > 0),
    CHECK (срок_годности_дни > 0),
    CHECK (единица_измерения IN ('кг', 'г', 'шт', 'уп', 'пак', 'банка')),
    CHECK (категория IN ('хлеб', 'булочные изделия', 'кондитерские изделия', 'мука', 'крупы', 'прочее'))
);
GO
CREATE TABLE поставки (
    id_поставки INT IDENTITY(1,1) PRIMARY KEY,
    id_предприятия INT NOT NULL,
    id_сотрудника_ответственного INT NOT NULL,
    id_продукта INT NOT NULL,
    дата_поставки DATE NOT NULL DEFAULT GETDATE(),
    количество DECIMAL(10,3) NOT NULL,
    цена_на_момент_поставки DECIMAL(10,2) NOT NULL,
    сумма_поставки DECIMAL(12,2) NOT NULL,
    статус_поставки NVARCHAR(20) NOT NULL DEFAULT 'в обработке',
    номер_накладной NVARCHAR(50) UNIQUE,
    CHECK (количество > 0),
    CHECK (цена_на_момент_поставки > 0),
    CHECK (сумма_поставки = количество * цена_на_момент_поставки),
    CHECK (статус_поставки IN ('в обработке', 'подтверждена', 'в пути', 'доставлена', 'отменена')),
    FOREIGN KEY (id_предприятия) REFERENCES предприятия_розничной_торговли(id_предприятия),
    FOREIGN KEY (id_сотрудника_ответственного) REFERENCES сотрудники(id_сотрудника),
    FOREIGN KEY (id_продукта) REFERENCES продукты(id_продукта)
);
