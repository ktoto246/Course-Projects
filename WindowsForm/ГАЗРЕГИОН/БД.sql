CREATE DATABASE ГАЗРЕГИОН;
GO
USE ГАЗРЕГИОН;
GO
CREATE TABLE отделы (
    id_отдела INT PRIMARY KEY IDENTITY,
    название_отдела VARCHAR(100) NOT NULL,
    описание_отдела NVARCHAR(50),
    дата_создания DATE DEFAULT GETDATE()
);
CREATE TABLE сотрудники (
    id_сотрудника INT PRIMARY KEY IDENTITY,
    id_отдела INT,
    табельный_номер VARCHAR(20) UNIQUE NOT NULL,
    фамилия VARCHAR(50) NOT NULL,
    имя VARCHAR(50) NOT NULL,
    отчество VARCHAR(50),
    должность VARCHAR(100) NOT NULL,
    телефон VARCHAR(20),
    email VARCHAR(100),
    дата_приема DATE,
    статус NVARCHAR(50) DEFAULT 'работает' CHECK(статус IN('работает', 'уволен', 'отпуск', 'больничный')),
    FOREIGN KEY (id_отдела) REFERENCES отделы(id_отдела)
);
CREATE TABLE проекты (
    id_проекта INT PRIMARY KEY IDENTITY,
    название_проекта VARCHAR(200) NOT NULL,
    описание_проекта NVARCHAR(100),
    id_руководителя INT,
    дата_начала DATE,
    дата_окончания DATE,
    статус_проекта NVARCHAR(50) DEFAULT 'планируется' CHECK(статус_проекта IN ('планируется', 'в работе', 'приостановлен', 'завершен')),
    бюджет DECIMAL(15,2),
    FOREIGN KEY (id_руководителя) REFERENCES сотрудники(id_сотрудника)
);
CREATE TABLE задачи (
    id_задачи INT PRIMARY KEY IDENTITY,
    id_проекта INT,
    id_исполнителя INT,
    название_задачи VARCHAR(200) NOT NULL,
    описание_задачи TEXT,
    приоритет NVARCHAR(50) DEFAULT 'средний' CHECK(приоритет IN('низкий', 'средний', 'высокий', 'критический')),
    статус_задачи NVARCHAR(50) DEFAULT 'к выполнению' CHECK(статус_задачи IN('к выполнению', 'в работе', 'на проверке', 'выполнена', 'отменена')),
    дата_создания DATE DEFAULT GETDATE(),
    срок_выполнения DATE,
    дата_завершения DATE,
    FOREIGN KEY (id_проекта) REFERENCES проекты(id_проекта),
    FOREIGN KEY (id_исполнителя) REFERENCES сотрудники(id_сотрудника)
);
