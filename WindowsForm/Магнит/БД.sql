CREATE DATABASE Магнит_Должности;
GO

USE Магнит_Должности;
GO
CREATE TABLE Отделы (
    id_отдела INT IDENTITY(1,1) PRIMARY KEY,
    название_отдела NVARCHAR(100) NOT NULL,
    описание_отдела NVARCHAR(500),
    дата_создания DATE DEFAULT GETDATE(),
    CONSTRAINT CHK_название_отдела CHECK (LEN(название_отдела) >= 2 AND название_отдела NOT LIKE '%[0-9]%')
);
GO
CREATE TABLE Должности (
    id_должности INT IDENTITY(1,1) PRIMARY KEY,
    id_отдела INT NOT NULL,
    название_должности NVARCHAR(100) NOT NULL,
    уровень_должности NVARCHAR(50) NOT NULL,
    минимальная_зарплата DECIMAL(10,2) NOT NULL,
    максимальная_зарплата DECIMAL(10,2) NOT NULL,
    описание_обязанностей NVARCHAR(1000),
    требования NVARCHAR(500),
    дата_создания DATE DEFAULT GETDATE(),
    CONSTRAINT FK_Должности_Отделы FOREIGN KEY (id_отдела) REFERENCES Отделы(id_отдела),
    CONSTRAINT CHK_название_должности CHECK (LEN(название_должности) >= 2),
    CONSTRAINT CHK_уровень_должности CHECK (уровень_должности IN ('Стажер', 'Младший', 'Средний', 'Старший', 'Ведущий', 'Руководитель')),
    CONSTRAINT CHK_зарплата CHECK (минимальная_зарплата > 0 AND максимальная_зарплата >= минимальная_зарплата AND максимальная_зарплата <= 1000000)
);
GO
CREATE TABLE Сотрудники (
    id_сотрудника INT IDENTITY(1,1) PRIMARY KEY,
    id_должности INT NOT NULL,
    фио NVARCHAR(150) NOT NULL,
    телефон NVARCHAR(20),
    email NVARCHAR(100),
    дата_приема DATE NOT NULL,
    дата_рождения DATE NOT NULL,
    статус NVARCHAR(20) DEFAULT 'Активен',
    CONSTRAINT FK_Сотрудники_Должности FOREIGN KEY (id_должности) REFERENCES Должности(id_должности),
    CONSTRAINT CHK_фио CHECK (LEN(фио) >= 5 AND фио LIKE '% % %'),
    CONSTRAINT CHK_телефон CHECK (телефон LIKE '+7([0-9][0-9][0-9])[0-9][0-9][0-9]-[0-9][0-9]-[0-9][0-9]'),
    CONSTRAINT CHK_email CHECK (email LIKE '%@%.%'),
    CONSTRAINT CHK_дата_рождения CHECK (дата_рождения <= DATEADD(YEAR, -18, GETDATE()) AND дата_рождения >= DATEADD(YEAR, -70, GETDATE())),
    CONSTRAINT CHK_статус CHECK (статус IN ('Активен', 'Уволен', 'Отпуск', 'Больничный'))
);
GO
