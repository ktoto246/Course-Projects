CREATE DATABASE MedicalClinic;
GO

USE MedicalClinic;
GO

-- Таблица сотрудников (врачей и медперсонала)
CREATE TABLE Сотрудники (
    id_сотрудника INT IDENTITY(1,1) PRIMARY KEY,
    фамилия NVARCHAR(50) NOT NULL,
    имя NVARCHAR(50) NOT NULL,
    отчество NVARCHAR(50),
    должность NVARCHAR(50) NOT NULL,
    специализация NVARCHAR(100),
    телефон NVARCHAR(20),
    email NVARCHAR(100),
    дата_приема DATE NOT NULL DEFAULT GETDATE(),
    статус NVARCHAR(20) NOT NULL DEFAULT 'Активен',
    CHECK (должность IN ('Терапевт', 'Хирург', 'Кардиолог', 'Невролог', 'Педиатр', 'Медсестра', 'Администратор')),
    CHECK (статус IN ('Активен', 'Отпуск', 'Больничный', 'Уволен'))
);

-- Таблица пациентов
CREATE TABLE Пациенты (
    id_пациента INT IDENTITY(1,1) PRIMARY KEY,
    фамилия NVARCHAR(50) NOT NULL,
    имя NVARCHAR(50) NOT NULL,
    отчество NVARCHAR(50),
    дата_рождения DATE NOT NULL,
    пол NVARCHAR(1) NOT NULL,
    адрес NVARCHAR(200),
    телефон NVARCHAR(20),
    страховой_полис NVARCHAR(20) UNIQUE,
    дата_регистрации DATE NOT NULL DEFAULT GETDATE(),
    CHECK (пол IN ('М', 'Ж'))
);

-- Таблица медицинских записей (приемы, диагнозы, назначения)
CREATE TABLE Медицинские_записи (
    id_записи INT IDENTITY(1,1) PRIMARY KEY,
    id_пациента INT NOT NULL,
    id_врача INT NOT NULL,
    дата_приема DATETIME NOT NULL DEFAULT GETDATE(),
    жалобы NVARCHAR(500),
    диагноз NVARCHAR(200) NOT NULL,
    назначенное_лечение NVARCHAR(500),
    примечания NVARCHAR(1000),
    статус NVARCHAR(20) NOT NULL DEFAULT 'Завершен',
    следующая_явка DATE,
    FOREIGN KEY (id_пациента) REFERENCES Пациенты(id_пациента) ON DELETE CASCADE,
    FOREIGN KEY (id_врача) REFERENCES Сотрудники(id_сотрудника),
    CHECK (статус IN ('Запланирован', 'В процессе', 'Завершен', 'Отменен'))
);

