-- Создание базы данных
CREATE DATABASE КФХ_Горбов_НИ;
GO

USE КФХ_Горбов_НИ;
GO

-- 1. Таблица сотрудников
CREATE TABLE сотрудники (
    id_сотрудника INT IDENTITY(1,1) PRIMARY KEY,
    фамилия NVARCHAR(50) NOT NULL,
    имя NVARCHAR(50) NOT NULL,
    отчество NVARCHAR(50),
    должность NVARCHAR(50) NOT NULL,
    дата_приема DATE NOT NULL,
    оклад DECIMAL(10,2) NOT NULL CHECK (оклад > 0),
    телефон NVARCHAR(20),
    CHECK (дата_приема <= CAST(GETDATE() AS DATE)),
    CHECK (LEN(телефон) >= 10)
);

-- 2. Таблица сельхоз_культур
CREATE TABLE сельхоз_культуры (
    id_культуры INT IDENTITY(1,1) PRIMARY KEY,
    название_культуры NVARCHAR(100) NOT NULL,
    тип_культуры NVARCHAR(50) NOT NULL CHECK (тип_культуры IN ('зерновые', 'овощи', 'фрукты', 'технические')),
    срок_созревания_дни INT NOT NULL CHECK (срок_созревания_дни > 0),
    стоимость_посевного_материала DECIMAL(10,2) CHECK (стоимость_посевного_материала >= 0),
    урожайность_т_га DECIMAL(8,2) CHECK (урожайность_т_га >= 0)
);

-- 3. Таблица участки
CREATE TABLE участки (
    id_участка INT IDENTITY(1,1) PRIMARY KEY,
    название_участка NVARCHAR(100) NOT NULL,
    ответственный_сотрудник INT NOT NULL,
    площадь_га DECIMAL(10,2) NOT NULL CHECK (площадь_га > 0),
    id_культуры INT,
    дата_посева DATE,
    планируемая_дата_сбора DATE,
    FOREIGN KEY (ответственный_сотрудник) REFERENCES сотрудники(id_сотрудника),
    FOREIGN KEY (id_культуры) REFERENCES сельхоз_культуры(id_культуры),
    CHECK (площадь_га <= 1000),
    CHECK (планируемая_дата_сбора > дата_посева OR планируемая_дата_сбора IS NULL)
);

-- 4. Таблица финансовые_операции
CREATE TABLE финансовые_операции (
    id_операции INT IDENTITY(1,1) PRIMARY KEY,
    дата_операции DATE NOT NULL,
    тип_операции NVARCHAR(50) NOT NULL CHECK (тип_операции IN ('доход', 'расход')),
    сумма DECIMAL(15,2) NOT NULL CHECK (сумма > 0),
    категория NVARCHAR(100) NOT NULL,
    описание NVARCHAR(500),
    id_сотрудника INT NOT NULL,
    id_участка INT,
    FOREIGN KEY (id_сотрудника) REFERENCES сотрудники(id_сотрудника),
    FOREIGN KEY (id_участка) REFERENCES участки(id_участка),
    CHECK (дата_операции <= CAST(GETDATE() AS DATE))
);
