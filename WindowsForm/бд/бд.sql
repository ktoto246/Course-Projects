GO

USE бд;
GO
CREATE TABLE сотрудники (
    id_сотрудника INT IDENTITY(1,1) PRIMARY KEY,
    фамилия NVARCHAR(50) NOT NULL,
    имя NVARCHAR(50) NOT NULL,
    отчество NVARCHAR(50),
    должность NVARCHAR(50) NOT NULL,
    телефон NVARCHAR(20),
    дата_приема DATE NOT NULL DEFAULT GETDATE(),
    CHECK (LEN(фамилия) >= 2),
    CHECK (LEN(имя) >= 2),
    CHECK (должность IN ('Пекарь', 'Упаковщик', 'Кладовщик', 'Менеджер', 'Директор', 'Продавец'))
);
CREATE TABLE товары (
    id_товара INT IDENTITY(1,1) PRIMARY KEY,
    название_товара NVARCHAR(100) NOT NULL,
    категория NVARCHAR(50) NOT NULL,
    вес_граммы INT NOT NULL,
    срок_годности_часы INT NOT NULL,
    цена_производства DECIMAL(10,2) NOT NULL,
    цена_продажи DECIMAL(10,2) NOT NULL,
    CHECK (вес_граммы > 0),
    CHECK (срок_годности_часы > 0),
    CHECK (цена_производства >= 0),
    CHECK (цена_продажи >= цена_производства),
    CHECK (категория IN ('Хлеб белый', 'Хлеб черный', 'Булочные изделия', 'Кондитерские изделия', 'Пироги'))
);
CREATE TABLE производство (
    id_производства INT IDENTITY(1,1) PRIMARY KEY,
    id_товара INT NOT NULL,
    id_сотрудника INT NOT NULL,
    количество INT NOT NULL,
    дата_производства DATE NOT NULL DEFAULT GETDATE(),
    статус NVARCHAR(20) NOT NULL DEFAULT 'Завершено',
    CHECK (количество > 0),
    CHECK (статус IN ('В процессе', 'Завершено', 'Отменено')),
    FOREIGN KEY (id_товара) REFERENCES товары(id_товара),
    FOREIGN KEY (id_сотрудника) REFERENCES сотрудники(id_сотрудника)
);
CREATE TABLE продажи (
    id_продажи INT IDENTITY(1,1) PRIMARY KEY,
    id_товара INT NOT NULL,
    id_сотрудника INT NOT NULL,
    количество INT NOT NULL,
    дата_продажи DATETIME NOT NULL DEFAULT GETDATE(),
    сумма DECIMAL(10,2) NOT NULL,
    CHECK (количество > 0),
    CHECK (сумма > 0),
    FOREIGN KEY (id_товара) REFERENCES товары(id_товара),
    FOREIGN KEY (id_сотрудника) REFERENCES сотрудники(id_сотрудника)
);
