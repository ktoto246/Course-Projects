CREATE DATABASE УчетАвтотранспорта;
GO

USE УчетАвтотранспорта;
GO

CREATE TABLE Автомобили (
    ID_Авто INT IDENTITY(1,1) PRIMARY KEY,
    Гос_Номер VARCHAR(20) UNIQUE NOT NULL,
    Марка_Модель VARCHAR(100) NOT NULL,
    Год_Выпуска INT NOT NULL,
    Текущий_Пробег INT DEFAULT 0,
    Статус VARCHAR(50) DEFAULT 'Активен' 
);
GO

CREATE TABLE Автопробег (
    ID_Записи INT IDENTITY(1,1) PRIMARY KEY,
    ID_Авто INT REFERENCES Автомобили(ID_Авто) ON DELETE CASCADE,
    Дата_Поездки DATE NOT NULL,
    Пройдено_Км INT NOT NULL,
    Водитель VARCHAR(100),
    Путевой_Лист VARCHAR(50)
);
GO

CREATE TABLE Плановые_Ремонты (
    ID_Ремонта INT IDENTITY(1,1) PRIMARY KEY,
    ID_Авто INT REFERENCES Автомобили(ID_Авто) ON DELETE CASCADE,
    Тип_Ремонта VARCHAR(100) NOT NULL,
    Плановая_Дата DATE,
    Плановый_Пробег INT,
    Фактическая_Дата DATE,
    Статус VARCHAR(50) DEFAULT 'Запланирован',
    Описание TEXT
);
GO

