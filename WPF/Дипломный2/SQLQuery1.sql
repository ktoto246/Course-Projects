CREATE TABLE Сотрудники (
    ID_Сотрудника INT IDENTITY(1,1) PRIMARY KEY,
    ФИО NVARCHAR(150) NOT NULL,
    Логин NVARCHAR(50) NOT NULL UNIQUE,
    Пароль NVARCHAR(256) NOT NULL,
    Роль NVARCHAR(30) NOT NULL 
        CHECK (Роль IN ('Администратор', 'Весовщик', 'Лаборант', 'Менеджер')),
    Статус_Активности BIT DEFAULT 1
);

CREATE TABLE Клиенты (
    ID_Клиента INT IDENTITY(1,1) PRIMARY KEY,
    Название_Организации NVARCHAR(150) NOT NULL,
    ИНН NVARCHAR(12) NOT NULL UNIQUE,
    Адрес NVARCHAR(250) NULL,
    Контактный_Телефон NVARCHAR(20) NULL
);

CREATE TABLE Справочник_Культур (
    ID_Культуры INT IDENTITY(1,1) PRIMARY KEY,
    Название_Культуры NVARCHAR(50) NOT NULL UNIQUE,
    Базовая_Влажность_Процент DECIMAL(4,2) NOT NULL,
    Базовая_Сорность_Процент DECIMAL(4,2) NOT NULL
);

CREATE TABLE Склады (
    ID_Склада INT IDENTITY(1,1) PRIMARY KEY,
    Наименование_Склада NVARCHAR(50) NOT NULL,
    Вместимость_Тонн DECIMAL(10,2) NOT NULL,
    Текущая_Загрузка_Тонн DECIMAL(10,2) DEFAULT 0
);

CREATE TABLE Услуги (
    ID_Услуги INT IDENTITY(1,1) PRIMARY KEY,
    Название_Услуги NVARCHAR(100) NOT NULL,
    Единица_Измерения NVARCHAR(20) NOT NULL,
    Цена_За_Единицу DECIMAL(10, 2) NOT NULL CHECK (Цена_За_Единицу >= 0)
);

CREATE TABLE Партии_Зерна (
    ID_Партии INT IDENTITY(1,1) PRIMARY KEY,
    ID_Клиента INT NOT NULL,
    ID_Весовщика INT NOT NULL,
    ID_Культуры INT NOT NULL,
    ID_Склада INT NULL,
    Номер_Авто NVARCHAR(20) NOT NULL,
    Вес_Брутто DECIMAL(10, 2) NOT NULL CHECK (Вес_Брутто > 0),
    Вес_Тара DECIMAL(10, 2) NOT NULL CHECK (Вес_Тара >= 0),
    Вес_Нетто AS (Вес_Брутто - Вес_Тара) PERSISTED,
    Дата_Прибытия DATETIME DEFAULT GETDATE(),
    Статус NVARCHAR(50) DEFAULT 'Ожидает анализа'
        CHECK (Статус IN ('Ожидает анализа', 'Проведен анализ', 'Завершено')),
    FOREIGN KEY (ID_Клиента) REFERENCES Клиенты(ID_Клиента),
    FOREIGN KEY (ID_Весовщика) REFERENCES Сотрудники(ID_Сотрудника),
    FOREIGN KEY (ID_Культуры) REFERENCES Справочник_Культур(ID_Культуры),
    FOREIGN KEY (ID_Склада) REFERENCES Склады(ID_Склада)
);

CREATE TABLE Лабораторные_Анализы (
    ID_Анализа INT IDENTITY(1,1) PRIMARY KEY,
    ID_Партии INT NOT NULL UNIQUE,
    ID_Лаборанта INT NOT NULL,
    Влажность_Факт DECIMAL(5, 2) NOT NULL,
    Сорность_Факт DECIMAL(5, 2) NOT NULL,
    Результат_Органолептики NVARCHAR(MAX) NULL,
    Дата_Анализа DATETIME DEFAULT GETDATE(),
    FOREIGN KEY (ID_Партии) REFERENCES Партии_Зерна(ID_Партии) ON DELETE CASCADE,
    FOREIGN KEY (ID_Лаборанта) REFERENCES Сотрудники(ID_Сотрудника)
);

CREATE TABLE Оказанные_Услуги (
    ID_Записи INT IDENTITY(1,1) PRIMARY KEY,
    ID_Партии INT NOT NULL,
    ID_Услуги INT NOT NULL,
    ID_Менеджера INT NOT NULL,
    Количество DECIMAL(10, 2) NOT NULL,
    Итоговая_Стоимость DECIMAL(12, 2) NOT NULL,
    Дата_Оформления DATETIME DEFAULT GETDATE(),
    FOREIGN KEY (ID_Партии) REFERENCES Партии_Зерна(ID_Партии) ON DELETE CASCADE,
    FOREIGN KEY (ID_Услуги) REFERENCES Услуги(ID_Услуги),
    FOREIGN KEY (ID_Менеджера) REFERENCES Сотрудники(ID_Сотрудника)
);

