CREATE DATABASE УчетНефтебазы;
GO

USE УчетНефтебазы;
GO

CREATE TABLE Нефтепродукты (
    ID_Продукта INT IDENTITY(1,1) PRIMARY KEY,
    Название VARCHAR(255) NOT NULL,
    Класс_Опасности INT NOT NULL,
    Цена_За_Тонну DECIMAL(12, 2) NOT NULL
);

CREATE TABLE Клиенты (
    ID_Клиента INT IDENTITY(1,1) PRIMARY KEY,
    Название_Компании VARCHAR(255) NOT NULL,
    ИНН VARCHAR(20) NOT NULL,
    Контактный_Телефон VARCHAR(50)
);

CREATE TABLE Поступления (
    ID_Поступления INT IDENTITY(1,1) PRIMARY KEY,
    ID_Продукта INT REFERENCES Нефтепродукты(ID_Продукта) ON DELETE CASCADE,
    Количество_Тонн DECIMAL(12, 2) NOT NULL,
    Дата_Поступления DATE NOT NULL
);

CREATE TABLE Реализация (
    ID_Реализации INT IDENTITY(1,1) PRIMARY KEY,
    ID_Продукта INT REFERENCES Нефтепродукты(ID_Продукта) ON DELETE CASCADE,
    ID_Клиента INT REFERENCES Клиенты(ID_Клиента) ON DELETE CASCADE,
    Количество_Тонн DECIMAL(12, 2) NOT NULL,
    Общая_Стоимость DECIMAL(12, 2) NOT NULL,
    Дата_Отгрузки DATE NOT NULL
);

